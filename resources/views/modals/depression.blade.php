                    <div class="col-10 col-sm-10  col-md-10 col-lg-10">
                        <div class="text-left">
                            <h2 class="text-green mt-2 ">Look For a Persistent Trend</h2>
                        </div>
                        <div class="mb-5">
                            <div class="row">
                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                            Persistently sad, anxious or generally low mood
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Everyone feels sad sometimes, has an anxious moment, or has a day when things feel a bit empty or low.
                                        However, when depression invades, it seems to keep you feeling persistently sad, low, anxious or empty,
                                        seemingly without cause. Some people feel 
                                        a combination of these four feelings. Others may predominantly just feel one of them.
                                        </span>
                                    </div>
                                </div>

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                            Loss of interest
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Do you find yourself feeling you can’t be bothered with things you used to find interesting or engaging?
                                        Depression thrives on cynical or ‘bored’ attitudes to life, and itself generates a sense of meaninglessness.
                                         This loss of interest may include loss of sexual interest, which can create strain in relationships, in turn allowing more room for depression.
                                        </span>
                                    </div>
                                </div>

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                            Loss of interest
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Do you find yourself feeling you can’t be bothered with things you used to find interesting or engaging?
                                        Depression thrives on cynical or ‘bored’ attitudes to life, and itself generates a sense of meaninglessness.
                                         This loss of interest may include loss of sexual interest, which can create strain in relationships, in turn allowing more room for depression.
                                        </span>
                                    </div>
                                </div>

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Lethargy or decreased energy
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Do you often feel unable to get up at the usual time? Or too lethargic to complete routine everyday tasks? Or do you generally find yourself 
                                        less able to maintain your usual activity levels? Depression may be sapping your energy and taking root as you succumb to lethargy.
                                        </span>
                                    </div>
                                </div>

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Irregular sleep or change in sleep pattern
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        It is very common for depression to have an effect on sleep, leading to insomnia and sleep disruptions like early waking.
                                        Sometimes there is a pattern of excessive sleep (hypersomnia). Regular, sound sleep is essential for healthy functioning, and sleep disruption is a particularly 
                                        debilitating weapon in the depression arsenal.
                                        </span>
                                    </div>
                                </div>

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Appetite or weight changes
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Regular healthy eating is essential for general well-being.
                                        Loss of appetite and weight loss, or sometimes overeating and weight gain, can be a sign that depression is interfering with
                                        the healthy eating habits essential for maintenance of mood levels and general functioning
                                        </span>
                                    </div>
                                </div>
                                
                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Increased tearfulness
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Crying serves a very useful purpose – stress hormones are released through tears.
                                        However, if you find yourself crying much more than usual, for no clear reasons, it may be a sign that depression is at work.
                                        </span>
                                    </div>
                                </div>

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Restlessness
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Do you find it hard to settle down to a task, or to sit still for any length of time? Some people are naturally energetic,
                                         but depression can bring a troubling sense of restlessness and inability to focus. Being constantly keyed up and over-alert in this way can be exhausting, in turn decreasing resistance to depression.
                                        </span>
                                    </div>
                                </div> 
                                
                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Poor concentration and difficulty making decisions
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Depression is often said to be a problem of disordered thinking, with ‘automatic
                                        negative thoughts’ crowding the mind. Poor concentration and/or difficulty solving problems or making decisions can be due to ‘blanking things out’, or may indicate the need to address the thinking habits that are allowing depression a foothold.
                                        </span>
                                    </div>
                                </div> 

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Hopelessness and pessimism
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        Having a generally pessimistic outlook can feel like the ‘safe’ approach in an uncertain world – “well, at least that way I won’t be disappointed”.
                                        However, depression thrives on this negative attitude, increasingly robbing you of hope and leaving its characteristic ’empty’ feeling instead.
                                        </span>
                                    </div>
                                </div> 

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Feelings of helplessness
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        There are many uncertainties in life, and things that cannot be controlled, yet many cultures seem to overstate the importance of individuals having ‘control’ over their lives. Having bad things happen and not being able to prevent them can then leave someone
                                        vulnerable to the generalised feeling of helplessness that depression feeds on and perpetuates.
                                        </span>
                                    </div>
                                </div> 

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Feelings of worthlessness or guilt
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                       
                                        Depression thrives on opportunities to promote over-harsh self-judgement and feelings of worthlessness. It causes people to inappropriately blame themselves for experiences like being badly treated or failing to meet unrealistic standards. It can also lead people to compare themselves negatively
                                        against others. All of this combined can lead to corrosive and unhelpful guilt.
                                        </span>
                                    </div>
                                </div> 

                                <div class="col-10 col-sm-10  col-md-10 col-lg-6 mt-2 ">
                                    <div class="my-2">
                                        <span class="font-weight-bold">
                                        Thoughts of death or suicide
                                        </span>
                                    </div>
                                    <div>
                                        <span class="">
                                        It is natural that people will think about death on occasions. However, the excessive negativity caused by depression can lead to repetitive, unhelpful dwelling on death. Depression also reduces problem-solving ability and causes increasing ‘tunnel vision’, falsely making suicide seem like a solution to problems. Repeatedly
                                        thinking about suicide can be very risky. It should be taken seriously and dealt with promptly.
                                        </span>
                                    </div>
